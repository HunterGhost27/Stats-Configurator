--	====================
--	DETERMINE STAT-TYPES
--	====================

---	Returns the statType of statName
---@param statName string
---@return string statType
function DetermineStatType(statName)
	if not ValidString(statName) then return end

	local statType = Osi.NRD_StatGetType(statName)

	--	SKILLDATA
	--	=========

	if statType == 'SkillData' then
		local super = Disintegrate(statName, "_")
		if SkillandStatusData['SkillData'][super] then return super end

	--	STATUSDATA
	--	==========
	elseif statType == 'StatusData' then
		local statusType = "Status_" .. Ext.StatGetAttribute(statName, 'StatusType')
		return statusType
	end

	return statType
end

---Returns the attributeType of attribute
---@param attribute string
---@param statName string
---@return string attributeType
function DetermineAttributeType(attribute, statName)
	local attributeType = StatAttributeDefs[attribute]
	if IsValid(attribute) then return attributeType end
	return type(Ext.StatGetAttribute(statName, attribute))
end

--	================
--	ENUM TRANSFORMER
--	================

---	Converts Enumeration Index and Labels
---@param mode string `Index2Label` or `Label2Index`
---@param enumerationTypeName string Enumeration Name
---@param argument string `Index` or `Label`
---@return string @`Index` or `Label`
function EnumTransformer(mode, enumerationTypeName, argument)
	if mode == "Index2Label" then return Ext.EnumIndexToLabel(enumerationTypeName, argument)
	elseif mode == "Label2Index" then return Ext.EnumLabelToIndex(enumerationTypeName, argument)
	end
end
